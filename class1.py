class Human:
    def __init__(self, name , age, gender):
        # Instance Attributes
        self.name = name
        self.age = age
        self.gender = gender

    def display_info(self):
        text = f"I am {self.name}  and I am {self.age} years old, {self.gender} "
        print(text)
        return text

james = Human("James", 32, "Male")
james.display_info()


